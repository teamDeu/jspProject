package report;

public class SuspensionBean {
	private int suspension_num;
	private String suspension_id;
	private String suspension_date;
	private int suspension_type;
	private int suspension_reason;
	
	public int getSuspension_reason() {
		return suspension_reason;
	}
	public void setSuspension_reason(int suspension_reason) {
		this.suspension_reason = suspension_reason;
	}
	public int getSuspension_type() {
		return suspension_type;
	}
	public void setSuspension_type(int suspension_type) {
		this.suspension_type = suspension_type;
	}
	public int getSuspension_num() {
		return suspension_num;
	}
	public void setSuspension_num(int suspension_num) {
		this.suspension_num = suspension_num;
	}
	public String getSuspension_id() {
		return suspension_id;
	}
	public void setSuspension_id(String suspension_id) {
		this.suspension_id = suspension_id;
	}
	public String getSuspension_date() {
		return suspension_date;
	}
	public void setSuspension_date(String suspension_date) {
		this.suspension_date = suspension_date;
	}

	
	
}
