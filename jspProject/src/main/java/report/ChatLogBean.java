package report;

public class ChatLogBean {
	private int chatlog_num;
	private String chatlog_id;
	private String chatlog_content;
	private String chatlog_at;
	public int getChatlog_num() {
		return chatlog_num;
	}
	public void setChatlog_num(int chatlog_num) {
		this.chatlog_num = chatlog_num;
	}
	public String getChatlog_id() {
		return chatlog_id;
	}
	public void setChatlog_id(String chatlog_id) {
		this.chatlog_id = chatlog_id;
	}
	public String getChatlog_content() {
		return chatlog_content;
	}
	public void setChatlog_content(String chatlog_content) {
		this.chatlog_content = chatlog_content;
	}
	public String getChatlog_at() {
		return chatlog_at;
	}
	public void setChatlog_at(String chatlog_at) {
		this.chatlog_at = chatlog_at;
	}
	
	
}
