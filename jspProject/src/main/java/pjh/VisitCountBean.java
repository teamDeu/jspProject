package pjh;

public class VisitCountBean {
	private String user_id;
	private int visit_today;
	private int visit_all;
	public String getUser_id() {	
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public int getVisit_today() {
		return visit_today;
	}
	public void setVisit_today(int visit_today) {
		this.visit_today = visit_today;
	}
	public int getVisit_all() {
		return visit_all;
	}
	public void setVisit_all(int visit_all) {
		this.visit_all = visit_all;
	}
	
	
}
