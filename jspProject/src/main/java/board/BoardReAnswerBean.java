package board;

public class BoardReAnswerBean {
	private int reanswer_num;
	private int answer_num;
	private String reanswer_content;
	private String reanswer_id;
	private String reanswer_at;
	
	
	
	
	public int getReanswer_num() {
		return reanswer_num;
	}
	
	public void setReanswer_num(int reanswer_num) {
		this.reanswer_num = reanswer_num;
	}
	
	public int getAnswer_num() {
		return answer_num;
	}
	
	public void setAnswer_num(int answer_num) {
		this.answer_num = answer_num;
	}
	
	public String getReanswer_content() {
		return reanswer_content;
	}
	
	public void setReanswer_content(String reanswer_content) {
		this.reanswer_content = reanswer_content;
	}
	
	public String getReanswer_id() {
		return reanswer_id;
	}
	
	public void setReanswer_id(String reanswer_id) {
		this.reanswer_id = reanswer_id;
	}
	
	public String getReanswer_at() {
		return reanswer_at;
	}
	
	public void setReanswer_at(String reanswer_at) {
		this.reanswer_at = reanswer_at;
	}
}
